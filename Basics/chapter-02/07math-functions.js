
// JS Math Functions 
// Functions are core building blocks of any programming language.

console.log(Math.E); // 2.718281828459045
console.log(Math.PI); // 3.141592653589793

var n = 4.589;
console.log(Math.abs(n)); // 4.589 (Absolute Number)
console.log(Math.floor(n)); // 4
console.log(Math.ceil(n)); // 5
console.log(Math.round(n)); // 5

console.log(Math.max(400, 500, 600)); // 600
console.log(Math.min(400, 500, 200)); // 200

console.log(Math.pow(2, 3)); // 8
console.log(Math.pow(3, 5)); // 243
console.log(Math.sqrt(9)); // 3
console.log(Math.sqrt(64)); // 8

console.log(Math.random()); // 0.8149749732226843 (Genarate random number between 0 to 1)
console.log(Math.random() * 50 + 1); // 35.75640412600109 (between 1 to 50)
console.log(Math.round(Math.random() * 50 + 1)); // 10 (between 1 to 50)

